package info.reflectionsofmind.vijual.core.lazy.exception;

public class TypingException extends RuntimeException
{
	public TypingException()
	{
		super();
	}

	public TypingException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

	public TypingException(String arg0)
	{
		super(arg0);
	}

	public TypingException(Throwable arg0)
	{
		super(arg0);
	}
}
