package info.reflectionsofmind.vijual.core.lazy.exception;

public class EvaluationException extends TypingException
{

	public EvaluationException()
	{
		super();
	}

	public EvaluationException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

	public EvaluationException(String arg0)
	{
		super(arg0);
	}

	public EvaluationException(Throwable arg0)
	{
		super(arg0);
	}

}
